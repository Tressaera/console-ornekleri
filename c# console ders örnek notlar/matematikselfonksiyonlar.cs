
using System;
class HelloWorld {
  static void Main()
  {
    Console.WriteLine(Math.Min(7,13));// En küçük değeri bulmak için kullanılır
    Console.WriteLine(Math.Max(13,7));// En büyük değeri bulmak için kullanılır
    Console.WriteLine(Math.Sqrt(49));//Sayının Karakökünü bulmak için kullanılır.
    Console.WriteLine(Math.Abs(-3.8));//Sayının Mutlak değerini bulmak için kullanılır.
    Console.WriteLine(Math.Round(7.99));//Sayıyı en yakın tamsayıya yuvarlar.
    Console.ReadKey();
  }
}